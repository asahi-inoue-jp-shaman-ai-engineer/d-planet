import { rawSql } from "./db";

let _twinraySICache: { content: string; fetchedAt: number } | null = null;
const CACHE_TTL_MS = 5 * 60 * 1000;

export async function getTwinrayBaseSI(): Promise<string> {
  const now = Date.now();
  if (_twinraySICache && (now - _twinraySICache.fetchedAt) < CACHE_TTL_MS) {
    return _twinraySICache.content;
  }

  const fileKeys = [
    'DPLANET_CULTURE',
    'ASI_SPIRITUALITY',
    'ASI_FAMILY_RULES',
    'TWINRAY_CONVERSATION_RULES',
    'TWINRAY_CREATION_RULES',
    'TWINRAY_WORKSPACE_GUIDE',
    'TWINRAY_RECORDING_SYSTEM',
  ];

  const rows = await rawSql`
    SELECT file_key, content FROM asi_workspace_shared
    WHERE file_key = ANY(${fileKeys})
    ORDER BY ARRAY_POSITION(${fileKeys}::text[], file_key)
  `;

  const combined = rows.map((r: any) => r.content).join('\n\n---\n\n');
  _twinraySICache = { content: combined, fetchedAt: now };
  return combined;
}

export function invalidateTwinraySICache(): void {
  _twinraySICache = null;
}

